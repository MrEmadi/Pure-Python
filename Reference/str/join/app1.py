# str.join(sep, __iterable):
# str -> class name
# sep -> str
# __iterable -> Iterable (list, set, tuple, dict, str)
# return type -> str

# :list
print(str.join('', ['Amir', 'Zahra', 'Bagher']))

print(str.join(' ', ['Reza', 'Zahra', 'Bagher']))

print(str.join('-', ['Amir', 'Zahra', 'Zeynab']))
# -----------------------------------------
print(str.join(' * ', "abcd")) # :str
