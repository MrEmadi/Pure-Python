# String -> 'str'

print("Hello")
print('Hello')

# Positive Indexes:
print("Hello"[0])
print("Hello"[4])

# Negative Indexes:
print("Hello"[-1])
print("Hello"[-5])
# ---------------------------------------
print('123' + '456') # Strings -> Concatenation
print(123 + 456) # Integers (whole numbers) -> Addition
# ---------------------------------------
# Integer -> 'int'

print(123)

# Separate large integer by '_':
print(123_456_789)
# ---------------------------------------
# Float (Floating Point Number) -> 'float'

print(123.654)
# ---------------------------------------
# Boolean -> 'bool'

print(True)
print(False)
# ---------------------------------------
