# Integers and Floats (Numbers):

# Subtraction:
print(3 - 2)
# -----------------------------
# Addition:
print(7 + 8)
# -----------------------------
# Multiplication:
print(3 * 5)
# -----------------------------
# Division:
print(11 / 3) # :float

print(11 // 3) # :int -> warning (lose data!)
# -----------------------------
# Exponent:
print(2 ** 3)
# -----------------------------
# Modulo -> %:
print(80 % 2)
print(10 % 3)
