# foreach loop Syntax

# for <variable_name> in <sequence>:
#   ...
# sequence -> list, str, tuple, dict, set

names = ['Zahra', 'Amir', 'Bagher']

# list:
for name in names:
    print(f"[{name}]")
# ----------------------------------------
username = "Amirhossein"

# str:
for letter in username: print(f"[{letter}]")
