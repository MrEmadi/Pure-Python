# Normal Strings:

print("Amirhossein Emadi") # double quotes -> "..."

print('Amirhossein Emadi') # single quotes -> '...'
# -------------------------------------------
# Multi-line Strings -> """...""" or '''...''':

print("""
-> Amirhossein Emadi
   - Gorgan
   - 25 years old
   - male
""")

print('''
-> Amirhossein Emadi
-> Gorgan
-> 25 years old
-> male
''')